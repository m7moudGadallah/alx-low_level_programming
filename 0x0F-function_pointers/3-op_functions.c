#include "3-calc.h"

/**
 * op_add - add 2 integers
 *
 * @a: integer number
 * @b: integer number
 *
 * Return: sum of 2 numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub 2 integers
 *
 * @a: integer number
 * @b: integer number
 *
 * Return: sub of 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul 2 integers
 *
 * @a: integer number
 * @b: integer number
 *
 * Return: mul of 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div 2 integers
 *
 * @a: integer number
 * @b: integer number
 *
 * Return: div of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod 2 integers
 *
 * @a: integer number
 * @b: integer number
 *
 * Return: mod of 2 numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
